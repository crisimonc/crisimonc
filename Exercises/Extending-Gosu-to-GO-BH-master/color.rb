class Color
  def self.black
    return Gosu::Color.argb(0xff_000000)
  end
  def self.gray
    return Gosu::Color.argb(0xff_808080)
  end
  def self.white
    return Gosu::Color.argb(0xff_ffffff)
  end
  def self.aqua
    return Gosu::Color.argb(0xff_00ffff)
  end
  def self.red
    return Gosu::Color.argb(0xff_ff0000)
  end
  def self.green
    return Gosu::Color.argb(0xff_00ff00)
  end
  def self.blue
    return Gosu::Color.argb(0xff_0000ff)
  end
  def self.yellow
    return Gosu::Color.argb(0xff_ffff00)
  end
  def self.fuchsia
    return Gosu::Color.argb(0xff_ff00ff)
  end
  def self.cyan
    return Gosu::Color.argb(0xff_00ffff)
  end
  def self.none
    return Gosu::Color.argb(0x00_000000)
  end
end
